  '#005c67': 'teal',
  '#1D9BF0': 'blue',
  '#1d9bf0': 'blue',
  '#10B981': 'green',
  '#10b981': 'green',
  '#F59E0B': 'amber',
  '#f59e0b': 'amber',
  '#EF4444': 'red',
  '#ef4444': 'red',
  '#8B5CF6': 'purple',
  '#8b5cf6': 'purple',
  '#EC4899': 'pink',
  '#ec4899': 'pink',
  '#0EA5E9': 'sky',
  '#0ea5e9': 'sky',
  '#F97316': 'orange',
  '#f97316': 'orange',
  '#14B8A6': 'mint',
  '#14b8a6': 'mint',
  '#C46EDE': 'oxy',
  '#c46ede': 'oxy',
